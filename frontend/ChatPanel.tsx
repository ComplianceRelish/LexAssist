/** Convert a single message to plain-text with light markdown */
function messageToText(msg: ChatMessage): string {
  const label = msg.role === 'user' ? '🗣️ YOU' : '⚖️ LEXASSIST AI';
  return `${label}\n${'─'.repeat(40)}\n${msg.content}\n`;
}

/** Copy full conversation to clipboard as formatted text */
async function copyConversation(messages: ChatMessage[]): Promise<boolean> {
  const header = `LEXASSIST AI — LEGAL CONSULTATION\nDate: ${new Date().toLocaleDateString('en-IN', { day: '2-digit', month: 'long', year: 'numeric' })}\n${'═'.repeat(50)}\n\n`;
  const body = messages.map(messageToText).join('\n');
  const footer = `\n${'═'.repeat(50)}\nGenerated by LexAssist AI • Indian Law Specialist\nhttps://lex-assist.vercel.app\n`;
  try {
    await navigator.clipboard.writeText(header + body + footer);
    return true;
  } catch {
    return false;
  }
}

